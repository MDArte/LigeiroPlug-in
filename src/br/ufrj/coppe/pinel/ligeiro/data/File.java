package br.ufrj.coppe.pinel.ligeiro.data;

/**
 * @author Roque Pinel
 *
 */
public class File
{
	private String path;

	/**
	 * Default constructor.
	 */
	public File()
	{
		// empty
	}

	/**
	 * @return the path
	 */
	public String getPath()
	{
		return path;
	}

	/**
	 * @param path the path to set
	 */
	public void setPath(String path)
	{
		this.path = path;
	}
}
